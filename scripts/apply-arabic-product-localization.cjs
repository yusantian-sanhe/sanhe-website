const root = process.cwd();
const filePath = path.join(
  root, "src", "messages", "ar", "products.json"
);
const backupPath =
  `${filePath}.before-arabic-product-localization`;

if (!fs.existsSync(backupPath)) {
  fs.copyFileSync(filePath, backupPath);
}

const data = JSON.parse(
  fs.readFileSync(filePath, "utf8")
);

for (const [slug, translated] of Object.entries(
  translations
)) {
  data.products.items[slug] = {
    ...data.products.items[slug],
    ...translated,
  };

  data.products.detailSections.relatedProducts
    .items[slug] = {
      name: translated.name,
      description: translated.description,
    };
}

fs.writeFileSync(
  filePath,
  JSON.stringify(data, null, 2) + "\n",
  "utf8"
);

console.log(
  `Arabic localization updated: ${Object.keys(translations).length} products`
);
